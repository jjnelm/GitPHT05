/*
    VARIABLE === containers/boxes
    let - mutable/changeable values
    const - unmutable/unchangable values
    var - same as let, but has a few scoping differences; 
*/

let firstName = "Princess";
const myAge = 18;

//display in console
console.log("Firstname:" , firstName);
console.log("Age:" , myAge);

//editing let's value
firstName = "Ferds";
console.log("Firstname:" , firstName);

